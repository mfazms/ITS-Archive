package variabel_dan_operator;

public class Operator {
	
	public static void main(String[] args) {
		
		/*
		 * System.out.println(5/2); System.out.println(5.0/2.0);
		 * System.out.println(5.0/2); System.out.println(5/2.0);
		 * 
		 * float a = 2; System.out.println(5/a);
		 * 
		 * System.out.println(true & false); System.out.println(true | false);
		 * System.out.println(true && false); System.out.println(true || false);
		 */
		
		System.out.println(2 == 2);
		System.out.println(2 != 2);
		System.out.println(!false);
		
		System.out.println(5 >> 1);
		System.out.println(5 << 1);
	}

}
