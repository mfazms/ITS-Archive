package variabel_dan_operator;

public class PlusString {

	public static void main(String[] args) {
		
		/*
		 * String a = "saya"; String b = "makan";
		 * 
		 * System.out.println(a + " " + b);
		 * 
		 * System.out.println(3 + b); System.out.println(b + 3);
		 */
		
		int c = 3;
		int d = 5;
		
		System.out.println("Harga total = " + c + d);
		System.out.println(c + d + "Harga total = ");
		System.out.println("Harga total = " + (c + d));
		
		int total = c + d;
		
		System.out.println("Harga total = " + total);

	}

}
